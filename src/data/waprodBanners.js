/**
 * Get all WA Production banners
 * @returns {Array} - All WA Production banners
 */
export const getAllWAProdBanners = () => {
  return waprodBanners;
};

/**
 * Get WA Production banners by shape
 * @param {string} shape - The shape to filter by (1:1, horizontal, vertical)
 * @returns {Array} - Banners matching the specified shape
 */
export const getWAProdBannersByShape = (shape) => {
  return waprodBanners.filter(banner => banner.shape === shape);
};

/**
 * Get featured WA Production banners
 * @returns {Array} - All featured WA Production banners
 */
export const getFeaturedWAProdBanners = () => {
  return waprodBanners.filter(banner => banner.featured);
};

/**
 * Get banners for a specific product by name
 * @param {string} productName - The product name to filter by
 * @returns {Array} - Banners for the specified product
 */
export const getBannersByProduct = (productName) => {
  return waprodBanners.filter(banner => banner.product.includes(productName));
};

export default waprodBanners;
